import type { Task } from '@idlesummer/tasker'
import type { BuildContext } from '../types'
import { mkdir, writeFile } from 'fs/promises'
import { join } from 'path'
import { duration } from '@idlesummer/tasker'
import { PACKAGE_NAME } from '@/pen/constants'

// ===== Main Task =====

export const writeElementTreeFile: Task<BuildContext> = {
  name: 'Writing element-tree.ts',
  onSuccess: (_, dur) => `Saved element-tree.ts (${duration(dur)})`,
  run: async (ctx) => {
    const genDir = join(ctx.outDir, 'generated')
    const outDir = join(genDir, 'element-tree.ts')

    const code = [
      `// Auto-generated by ${PACKAGE_NAME}`,
      '// Do not manually edit this file',
      '// NOTE: This file is kept for documentation/debugging.',
      '',
      `import type { ElementTreeMap } from '${PACKAGE_NAME}'`,
      '',
      `export const elementTrees: ElementTreeMap = ${JSON.stringify(ctx.elementTrees!, null, 2)} as const`,
      '',
    ].join('\n')

    await mkdir(genDir, { recursive: true })
    await writeFile(outDir, code, 'utf-8')
  },
}
