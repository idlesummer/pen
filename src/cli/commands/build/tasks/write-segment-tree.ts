import type { Task } from '@idlesummer/tasker'
import type { BuildContext } from '../types'
import { mkdir, writeFile } from 'fs/promises'
import { join } from 'path'
import { duration } from '@idlesummer/tasker'
import { PACKAGE_NAME } from '@/pen/constants'

export const writeSegmentTree: Task<BuildContext> = {
  name: 'Writing segment-tree.ts',
  onSuccess: (_, dur) => `Saved segment-tree.ts (${duration(dur)})`,
  run: async (ctx) => {
    const genDir = join(ctx.outDir, 'artifacts')
    const outDir = join(genDir, 'segment-tree.ts')
    const code = [
      `// Auto-generated by ${PACKAGE_NAME}`,
      '// Do not manually edit this file',
      '',
      `import type { SegmentNode } from "${PACKAGE_NAME}"`,
      '',
      `export const segmentTree: SegmentNode = ${JSON.stringify(ctx.segmentTree!, null, 2)} as const`,
      '',
    ].join('\n')

    await mkdir(genDir, { recursive: true })
    await writeFile(outDir, code, 'utf-8')
  },
}
