import type { Task } from '@idlesummer/tasker'
import type { BuildContext } from '../types'
import { mkdir, writeFile } from 'fs/promises'
import { join } from 'path'
import { duration } from '@idlesummer/tasker'
import { PACKAGE_NAME } from '@/core/constants'

// ===== Main Task =====

export const writeComponentsFile: Task<BuildContext> = {
  name: 'Writing components.ts',
  onSuccess: (_, dur) => `Saved components.ts (${duration(dur)})`,
  run: async (ctx) => {
    const genDir = join(ctx.outDir, 'generated')
    const componentsPath = join(genDir, 'components.ts')
    await mkdir(genDir, { recursive: true })

    const entries = ctx.componentEntries!

    // Format component entries for readability
    const formattedEntries = entries.map((entry, index) => ({
      index,
      absolutePath: entry.absolutePath,
      importPath: entry.importPath,
    }))

    const code = [
      `// Auto-generated by ${PACKAGE_NAME}`,
      '// Do not manually edit this file',
      '// NOTE: This file is kept for documentation/debugging.',
      '// Shows the mapping between component indices and their file paths.',
      '',
      `import type { ComponentEntry } from '${PACKAGE_NAME}'`,
      '',
      'export type IndexedComponentEntry = ComponentEntry & { index: number }',
      '',
      `export const components: readonly IndexedComponentEntry[] = ${JSON.stringify(formattedEntries, null, 2)} as const`,
      '',
    ].join('\n')

    await writeFile(componentsPath, code, 'utf-8')
  },
}
