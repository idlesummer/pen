import type { Task } from '@idlesummer/tasker'
import type { BuildContext } from '../types'
import { mkdir, writeFile } from 'fs/promises'
import { join } from 'path'
import { duration } from '@idlesummer/tasker'
import { PACKAGE_NAME } from '@/pen/constants'

export const writeEntry: Task<BuildContext> = {
  name: 'Writing entry.ts',
  onSuccess: (_, dur) => `Saved entry.ts (${duration(dur)})`,
  run: async (ctx) => {
    const genDir = join(ctx.outDir, 'generated')
    const outDir = join(genDir, 'entry.ts')
    const code = [
      `// Auto-generated by ${PACKAGE_NAME}`,
      '// Do not manually edit this file',
      '',
      'import { createElement } from "react"',
      'import { render } from "ink"',
      `import { App } from '${PACKAGE_NAME}'`,
      'import { pathComponentMap } from "./path-component-map"',
      'import { routeTree } from "./route-tree"',
      '',
      'export async function run(initialUrl: string) {',
      '  const routingTable = { routeTree, pathComponentMap }',
      '  const element = createElement(App, { initialUrl, routingTable })',
      '  const { waitUntilExit } = render(element)',
      '  await waitUntilExit()',
      '}',
      '',
    ].join('\n')

    await mkdir(genDir, { recursive: true })
    await writeFile(outDir, code, 'utf-8')
  },
}
